public class Display_the_numbers {
	public static void main(String[] args)
	{
		int num1 = 1;
		int num2 = 2;
		int num3 = 3;
		int num4 = 4;
		System.out.println("Using System.out.println Statement");
		System.out.println(num1+" "+num2+" "+num3+" "+num4);
		System.out.println("using System.out.print statement");
		System.out.print(num1+" ");
		System.out.print(num2+" ");
		System.out.print(num3+" ");
		System.out.print(num4+" ");
		System.out.println();
		System.out.println("using printf statement:");
		System.out.printf("%d %d %d %d",num1,num2,num3,num4);
	}

}
